export function buildLevel(game, level){
    let blocks = [];

    level.forEach((row, rowIndex) => {
        row.forEach((block, blockIndex) => {
            if(block === 1){
                let position = {
                    x: 40 * blockIndex,
                    y: 40 * rowIndex
                };

                blocks.push(new Block(game, position));
            }
        })
    })
    return blocks;
}